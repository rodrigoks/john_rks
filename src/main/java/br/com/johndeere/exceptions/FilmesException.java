package br.com.johndeere.exceptions;

public class FilmesException extends Exception {

	public FilmesException() {
        super();
    }

    public FilmesException(String arg0) {
        super(arg0);
    }

    public FilmesException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public FilmesException(Throwable arg0) {
        super(arg0);
    }
    
}
