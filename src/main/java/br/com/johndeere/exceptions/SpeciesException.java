package br.com.johndeere.exceptions;

public class SpeciesException extends Exception {

	public SpeciesException() {
        super();
    }

    public SpeciesException(String arg0) {
        super(arg0);
    }

    public SpeciesException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public SpeciesException(Throwable arg0) {
        super(arg0);
    }
    
}
